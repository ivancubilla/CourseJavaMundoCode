public class Operaciones {

    public static void main(String[] args) {
        int numUno = 5,numDos = 2,suma,resta,multip,division,resto; //Declared variables

        //realized operations
        suma = numUno + numDos; //summa
        resta = numUno - numDos;  //subtraction
        multip = numUno * numDos; //multiplication
        division = numUno / numDos;  //division
        resto = numUno % numDos;     //remainder

        System.out.println("suma = " + suma); //print for screen the operations arithmetics
        System.out.println("resta = " + resta);
        System.out.println("Multiplicacion = " + multip);
        System.out.println("division = " + division);
        System.out.println("resto = " + resto);
    }
}
