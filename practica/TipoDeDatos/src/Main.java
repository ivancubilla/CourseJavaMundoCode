public class Main {
    public static void main(String[] args) {
        //tipos de datos no primitivos
        String nombrePersona = "Javier";

        //tipos de datos primitivos
        int edadPersona = 25; //entero
        double altura = 1.81; //decimales
        float altura2 = 1.55f;
        char incial = 'J'; //caracter
        boolean estadoCivil = true; //booleanos

        System.out.println("nombrePersona = " + nombrePersona);
        System.out.println("edadPersona = " + edadPersona);
        System.out.println("altura = " + altura);
        System.out.println("altura2 = " + altura2);
        System.out.println("incial = " + incial);
        System.out.println("estadoCivil = " + estadoCivil);
    }
}
